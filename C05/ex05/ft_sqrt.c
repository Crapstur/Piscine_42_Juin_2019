int		ft_sqrt(int nb)
{
	int i;
	int l;

	i = 0;
	l = 0;
	if (nb <= 0)
		return (0);
	while (l < nb && i <= 46340)
	{
		l = i * i;
		i++;
	}
	if (l == nb)
		return (i - 1);
	else
		return (0);
}
